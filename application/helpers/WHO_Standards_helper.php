/**
 * Get BMI-for-Age classification based on WHO standards
 * 
 * @param float $bmi Calculated BMI value
 * @param int $ageInMonths Age in months (72-228 months / 6-19 years)
 * @param string $sex 'M' or 'F'
 * @return string Classification: 'Severely Wasted', 'Wasted', 'Normal', 'Overweight', 'Obese'
 */
function getWHO_BMIClassification($bmi, $ageInMonths, $sex) {
    if ($bmi === null || $ageInMonths === null || empty($sex)) {
        return 'Normal';
    }
    
    $ageInMonths = (int)$ageInMonths;
    $sex = strtoupper($sex);
    
    // For children under 6 years (0-71 months), use weight-for-age instead
    // BMI standards are less reliable for under 5s
    if ($ageInMonths < 72) {
        return getSimpleBMIClassification($bmi);
    }
    
    // Get cutoff values for the specific age
    $cutoffs = getWHO_BMICutoffs($ageInMonths, $sex);
    
    if (!$cutoffs) {
        return 'Normal';
    }
    
    // Classify based on BMI value using Excel logic
    // Severely Wasted: BMI <= severe_wasted
    if ($bmi <= $cutoffs['severe_wasted']) {
        return 'Severely Wasted';
    }
    // Wasted: BMI between wasted_from and wasted_to (inclusive)
    elseif ($bmi >= $cutoffs['wasted_from'] && $bmi <= $cutoffs['wasted_to']) {
        return 'Wasted';
    }
    // Normal: BMI between normal_from and normal_to (inclusive)
    elseif ($bmi >= $cutoffs['normal_from'] && $bmi <= $cutoffs['normal_to']) {
        return 'Normal';
    }
    // Overweight: BMI between overweight_from and overweight_to (inclusive)
    elseif ($bmi >= $cutoffs['overweight_from'] && $bmi <= $cutoffs['overweight_to']) {
        return 'Overweight';
    }
    // Obese: BMI >= obese
    elseif ($bmi >= $cutoffs['obese']) {
        return 'Obese';
    }
    else {
        return 'Normal';
    }
}

/**
 * Simple BMI classification for children under 6 years
 */
function getSimpleBMIClassification($bmi) {
    if ($bmi < 14) return 'Severely Wasted';
    if ($bmi < 16) return 'Wasted';
    if ($bmi < 19) return 'Normal';
    if ($bmi < 22) return 'Overweight';
    return 'Obese';
}

/**
 * Get Height-for-Age classification based on WHO standards
 * 
 * @param float $heightCm Height in centimeters
 * @param int $ageInMonths Age in months (36-228 months / 3-19 years)
 * @param string $sex 'M' or 'F'
 * @return string Classification: 'Severely Stunted', 'Stunted', 'Normal', 'Tall'
 */
function getWHO_HeightForAgeClassification($heightCm, $ageInMonths, $sex) {
    if ($heightCm === null || $ageInMonths === null || empty($sex)) {
        return 'Normal';
    }
    
    $ageInMonths = (int)$ageInMonths;
    $sex = strtoupper($sex);
    
    // Get cutoff values for the specific age
    $cutoffs = getWHO_HeightCutoffs($ageInMonths, $sex);
    
    if (!$cutoffs) {
        return 'Normal';
    }
    
    // Classify based on height value
    if ($heightCm <= $cutoffs['severe_stunted']) {
        return 'Severely Stunted';
    } elseif ($heightCm >= $cutoffs['stunted_from'] && $heightCm <= $cutoffs['stunted_to']) {
        return 'Stunted';
    } elseif ($heightCm >= $cutoffs['normal_from'] && $heightCm <= $cutoffs['normal_to']) {
        return 'Normal';
    } elseif ($heightCm >= $cutoffs['tall']) {
        return 'Tall';
    } else {
        return 'Normal';
    }
}

/**
 * Get Weight-for-Age classification for under 5 years
