        return 'Normal';
    } elseif ($weightKg >= $cutoffs['overweight']) {
        return 'Overweight';
    } else {
        return 'Normal';
    }
}

/**
 * Get BMI cutoff values for a specific age and sex (6-19 years)
 */
function getWHO_BMICutoffs($ageInMonths, $sex) {
