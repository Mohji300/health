/**
 * Get Weight cutoff values for a specific age and sex (0-71 months)
 */
function getWHO_WeightCutoffs($ageInMonths, $sex) {
    if ($ageInMonths < 0 || $ageInMonths > 71) {
        return null;
    }
    
    if ($sex === 'M') {
        return getWHO_WeightCutoffs_Boys($ageInMonths);
    } else {
        return getWHO_WeightCutoffs_Girls($ageInMonths);
    }
}

