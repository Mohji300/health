 * @param int $ageInMonths Age in months (0-71 months)
 * @param string $sex 'M' or 'F'
 * @return string Classification: 'Severely Underweight', 'Underweight', 'Normal', 'Overweight'
 */
function getWHO_WeightForAgeClassification($weightKg, $ageInMonths, $sex) {
    if ($weightKg === null || $ageInMonths === null || empty($sex)) {
        return 'Normal';
    }
    
    $ageInMonths = (int)$ageInMonths;
    $sex = strtoupper($sex);
    
    // Get cutoff values for the specific age
    $cutoffs = getWHO_WeightCutoffs($ageInMonths, $sex);
    
    if (!$cutoffs) {
        return 'Normal';
    }
    
    // Classify based on weight value
    if ($weightKg <= $cutoffs['severe_underweight']) {
        return 'Severely Underweight';
    } elseif ($weightKg >= $cutoffs['underweight_from'] && $weightKg <= $cutoffs['underweight_to']) {
        return 'Underweight';
